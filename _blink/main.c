#include "stm32f0xx.h"
#include "stm32f0xx_rcc.h"
/*---------------------------------------------------------------------------*/
#include "digital.h"
#include "systick_delay.h"
/*---------------------------------------------------------------------------*/
static void hardware_init();
/*---------------------------------------------------------------------------*/
int main(void)
{  
  hardware_init();

  while(1)
  {
      _delay_ms(250);
      digitalWrite(B,1,HIGH);

      _delay_ms(250);
      digitalWrite(B,1,LOW);  
  }

  return 0;
}
/*---------------------------------------------------------------------------*/
static void hardware_init()
{
  systick_init(SYSTICK_1MS);

  RCC_AHBPeriphClockCmd(RCC_AHBPeriph_GPIOB, ENABLE);

  pinMode(B,1,OUTPUT);  

  _delay_ms(10);
}
